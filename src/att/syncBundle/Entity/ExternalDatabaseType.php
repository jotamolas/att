<?php

namespace att\syncBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * ExternalDatabaseType
 *
 * @ORM\Table(name="sync_external_database_type")
 * @ORM\Entity(repositoryClass="att\syncBundle\Repository\ExternalDatabaseTypeRepository")
 */
class ExternalDatabaseType
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="description", type="string", length=255)
     */
    private $description;



    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set description
     *
     * @param string $description
     *
     * @return ExternalDatabaseType
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }


}
